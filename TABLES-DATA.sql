CREATE TABLE EMPLOYEES (
    EMPLOYEE_ID   NUMBER PRIMARY KEY,
    FIRST_NAME    VARCHAR2(50),
    LAST_NAME     VARCHAR2(50),
    EMAIL         VARCHAR2(100) UNIQUE,
    PHONE_NUMBER  VARCHAR2(20),
    HIRE_DATE     DATE DEFAULT SYSDATE,
    JOB_ID        NUMBER,
    SALARY        NUMBER(10,2),
    COMMISSION_PCT NUMBER(5,2),
    MANAGER_ID    NUMBER,  -- Remove foreign key here temporarily
    DEPARTMENT_ID NUMBER
);

CREATE TABLE DEPARTMENTS (
    DEPARTMENT_ID   NUMBER PRIMARY KEY,
    DEPARTMENT_NAME VARCHAR2(100) UNIQUE,
    MANAGER_ID      NUMBER,
    LOCATION_ID     NUMBER
);

ALTER TABLE EMPLOYEES 
ADD CONSTRAINT FK_EMP_DEPT FOREIGN KEY (DEPARTMENT_ID) REFERENCES DEPARTMENTS(DEPARTMENT_ID);

ALTER TABLE EMPLOYEES 
ADD CONSTRAINT FK_EMP_MANAGER FOREIGN KEY (MANAGER_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID);


CREATE TABLE REGIONS (
    REGION_ID   NUMBER PRIMARY KEY,
    REGION_NAME VARCHAR2(50) NOT NULL
);

CREATE TABLE COUNTRIES (
    COUNTRY_ID   CHAR(2) PRIMARY KEY,
    COUNTRY_NAME VARCHAR2(50) NOT NULL,
    REGION_ID    NUMBER,
    FOREIGN KEY (REGION_ID) REFERENCES REGIONS(REGION_ID)
);


CREATE TABLE LOCATIONS (
    LOCATION_ID     NUMBER PRIMARY KEY,
    STREET_ADDRESS  VARCHAR2(100),
    POSTAL_CODE     VARCHAR2(20),
    CITY            VARCHAR2(50) NOT NULL,
    STATE_PROVINCE  VARCHAR2(50),
    COUNTRY_ID      CHAR(2),
    FOREIGN KEY (COUNTRY_ID) REFERENCES COUNTRIES(COUNTRY_ID)
);

CREATE TABLE JOBS (
    JOB_ID      NUMBER PRIMARY KEY,
    JOB_TITLE   VARCHAR2(50) NOT NULL,
    MIN_SALARY  NUMBER,
    MAX_SALARY  NUMBER
);

